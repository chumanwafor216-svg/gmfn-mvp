from __future__ import annotations

import json
from datetime import datetime, timezone
from io import BytesIO
from zipfile import ZipFile

from fastapi.testclient import TestClient
from sqlalchemy import create_engine
from sqlalchemy.orm import sessionmaker
from sqlalchemy.pool import StaticPool

from app.db.base import Base as CoreBase
from app.db.models import TrustEvent, User
from app.services.evidence_pack_service import (
    DEFAULT_FOOTER,
    LIMITATION_STATEMENT,
    PROTOCOL_VERSION,
    build_evidence_pack_meta,
    build_evidence_pack_zip,
)


def _session():
    engine = create_engine(
        "sqlite+pysqlite:///:memory:",
        connect_args={"check_same_thread": False},
        poolclass=StaticPool,
        future=True,
    )
    CoreBase.metadata.create_all(bind=engine)
    SessionLocal = sessionmaker(bind=engine, autoflush=False, autocommit=False, future=True)
    return engine, SessionLocal()


def test_evidence_pack_meta_faces_users_as_gsn():
    engine, db = _session()
    try:
        user = User(
            id=7,
            email="evidence@example.com",
            hashed_password="x",
            role="user",
            gmfn_id="GSN-USER-7",
        )
        meta = build_evidence_pack_meta(db, current_user=user)

        assert meta["pack_id"].startswith("GSN-PACK-U7-STANDARD-")
        assert meta["title"] == "GSN Evidence Pack"
        assert meta["surface_brand"] == "GSN"
        assert meta["surface_brand_full"] == "Global Support Network"
        assert meta["protocol_version"] == PROTOCOL_VERSION
        assert meta["footer"] == DEFAULT_FOOTER
        assert meta["limitation_statement"] == LIMITATION_STATEMENT
    finally:
        db.close()
        engine.dispose()


